//here shown that how the relational operators works.
public class relational {
    public static void main(String[] args) {
        int a = 10;
        int b = 10;
        System.out.println("a == b is : "+(a==b));
        System.out.println("a != b is : "+(a!=b));
        System.out.println("a < b is : "+(a<b));
        System.out.println("a > b is : "+(a>b));
        System.out.println("a <= b is : "+(a<=b));
        System.out.println("a >= b is : "+(a>=b));
    }
}
